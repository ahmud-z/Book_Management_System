    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        String id = inputId.getText();
        String password = inputPassword.getText();
        String role = buttonGroup1.getSelection().getActionCommand();

        String setAdminId = "admin";
        String setAdminPass = "123";
        StateManager.role = role;

        boolean isCorrect = false;

        if (role.equals("admin") && id.equals(setAdminId) && password.equals(setAdminPass)) {
            isCorrect = true;
        }

        if (role.equals("student") && id.equals("student") && password.equals("123")) {
            isCorrect = true;
        }

        if (isCorrect) {
            StateManager.previousWindow = this;

            MainWindow mainWindow = new MainWindow();
            mainWindow.setVisible(true);

            this.setVisible(false);

        } else {
            messageLabel.setText("Invalid input.");
            messageLabel.setForeground(Color.red);

        }
    }//GEN-LAST:event_loginButtonActionPerformed

