    public IssueBook() {
        initComponents();

        DefaultComboBoxModel model = new DefaultComboBoxModel();
        String[][] books = BookManager.getBooks();

        model.addElement("-- Select Book --");

        for (int i = 0; i < books.length; i++) {
            if (books[i][0] == null) {
                break;
            }
            model.addElement(books[i][0]);
        }

        issueBookComboBox.setModel(model);

    }

